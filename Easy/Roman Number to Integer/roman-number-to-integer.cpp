class Solution {
  public:
    int romanToDecimal(string &str) {
        // code here
            unordered_map<char, int> romanValues = {
        {'I', 1},
        {'V', 5},
        {'X', 10},
        {'L', 50},
        {'C', 100},
        {'D', 500},
        {'M', 1000}
    };

    int result = 0;
    int prevValue = 0;

    for (int i = str.length() - 1; i >= 0; i--) {
        int current = romanValues[str[i]];

        if (current < prevValue) {
            result -= current;
        } else {
            result += current;
        }

        prevValue = current;
    }

    return result;
    }
};

