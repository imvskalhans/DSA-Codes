/*
  Pairwise swap a linked list
  The input list will have at least one element
  node is defined as

struct Node
{
    int data;
    struct Node* next;

    Node(int x){
        data = x;
        next = NULL;
    }

}*head;
*/
class Solution
{
    public:
    Node* pairWiseSwap(struct Node* head) 
    {
        // The task is to complete this method
            // If the list is empty or has only one node, no swapping is needed.
    if (head == nullptr || head->next == nullptr) {
        return head;
    }

    // Initialize pointers for swapping.
    Node* prev = nullptr;
    Node* current = head;
    Node* nextNode = nullptr;

    // Traverse the list in pairs and swap nodes.
    while (current != nullptr && current->next != nullptr) {
        nextNode = current->next;
        current->next = nextNode->next;
        nextNode->next = current;

        // Adjust the previous node's next pointer.
        if (prev != nullptr) {
            prev->next = nextNode;
        } else {
            // Update the head if swapping starts from the first pair.
            head = nextNode;
        }

        // Move the pointers for the next pair.
        prev = current;
        current = current->next;
    }

    return head;
    }
};

