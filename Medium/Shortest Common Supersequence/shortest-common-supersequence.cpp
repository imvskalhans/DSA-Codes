class Solution
{
    public:
    //Function to find length of shortest common supersequence of two strings.
    int shortestCommonSupersequence(string X, string Y, int m, int n)
    {
        //code here
            // Create a 2D array to store the lengths of the common supersequences.
    vector<vector<int>> dp(m + 1, vector<int>(n + 1, 0));

    // Fill the dp array using bottom-up dynamic programming.
    for (int i = 0; i <= m; ++i) {
        for (int j = 0; j <= n; ++j) {
            if (i == 0 || j == 0) {
                dp[i][j] = i + j;
            } else if (X[i - 1] == Y[j - 1]) {
                dp[i][j] = 1 + dp[i - 1][j - 1];
            } else {
                dp[i][j] = 1 + min(dp[i - 1][j], dp[i][j - 1]);
            }
        }
    }

    // The length of the shortest common supersequence is stored in the bottom-right cell.
    return dp[m][n];
    }
};

